#include <iostream>

using namespace std;

int main() {
    
    char x = 'c';

    printf("The value of x is: [%c]\n", x); // %c is used to print a character
    printf("The value of x is: [%d]\n", x); // %d is used to print an integer
    printf("The value of x is: [%x]\n", x); // %x is used to print a hexadecimal
    printf("The value of x is: [%o]\n", x); // %o is used to print an octal

    return 0;

}