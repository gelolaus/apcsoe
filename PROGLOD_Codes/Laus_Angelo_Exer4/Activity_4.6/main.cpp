#include <cstdio>
#include <iostream>

using namespace std;

int main() {

    float f = 543.123456789;

    printf("Normal float output: [%f]\n", f);
    printf("Exponential float output: [%e]\n", f);
    printf("Normal float output with 2 decimal places: [%.2f]\n", f);

    return 0;

}