#include <cstdio>
#include <iostream>

using namespace std;

int main() {

    /* Declaring and initializing */
    int int_x = 100;
    long int long_int_x = 200;
    long long int long_long_int_x = 300;
    char char_x = 65;

    /* Printing of values */
    printf("This is printf value for int_x: [%d]\n", int_x);
    printf("This is printf value for long_int_x: [%ld]\n", long_int_x);
    printf("This is printf value for long_long_int_x: [%lld]\n", long_long_int_x);
    printf("This is printf value for char_x: [%d]\n", char_x);
    printf("THis is printf value for char_x: [%c]\n", char_x);

    return 0;

}